#!/usr/bin/env python3

str1="Hello World!"
print("The length of str1, given by len(str1) is {}".format(len(str1)))
print("The first character of the string is: {}".format(str1[0]))
#printing the last character of the string:
print("The last character of the string is: {}".format(str1[len(str1)-1]))

#printing the last character of the string counting from the right:
print("The last character of the string is: {}".format(str1[-1]))


